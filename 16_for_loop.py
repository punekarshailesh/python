# printing the elements of the list

friendsList = ['prathmesh' , 'datta' , 'abhishek','nikhil' , 'vinit']
print("List using for loop:")
for item in friendsList:
    print(item)


# printing tuples using for loop
NumbersTuple = (11 , 23.223235343 , float , True ,False , str , 'shailesh')

print("\nTuple using for loop:")
for i in NumbersTuple:
    print(i)


