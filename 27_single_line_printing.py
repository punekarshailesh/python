'''
   as print() function always prints in new line
   so 
     for printing all statements , values in one line we use ' end = " " ' 
     
  '''

#   eg. each print function prints in different line


print("How")
print("are")
print("you!")




# eg. each print function prints on same line by using end = " "
print('\nUsing end = "":')
print("How",end=' ')
print("are",end=" ")
print("you!",end="\n")

